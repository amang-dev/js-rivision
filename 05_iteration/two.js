//? for of (for iteration of array)


const array = [1,2,3,4,5]

for (const arr of array) {
    console.log(arr);
}

const greeting = "Hello World!"

for (const greet of greeting) {
    if(greet === " "){
        continue;
    }
    console.log(`the value is ${greet}`);
}


